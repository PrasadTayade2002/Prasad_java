package Gettersetter;

public class Student {
int rno;
String name;
String address;
public void setRno(int rno) {
	this.rno = rno;
}
public int getRno() {
	return rno;
}
public void setName(String name) {
	this.name = name;
}
public String getName() {
	return name;
}
public void setAddress(String address) {
	this.address = address;
}
public String getAddress() {
	return address;
}


}
