package Gettersetter;

public class Engine {
private float weight;
private String type;
private int cap;
public float getWeight() {
	return weight;
}
public void setWeight(float weight) {
	this.weight = weight;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public int getCap() {
	return cap;
}
public void setCap(int cap) {
	this.cap = cap;
}


}
