package Gettersetter;

public class Car {
	private String name;
	private String colour;
	private float price;
	private String type;
	private String  eng;
public void setName() {
	this.name=name;
}
public void setColour() {
	this.colour=colour;
}
public void setPrice() {
	this.price=price;
}
public void setType() {
	this.type=type;
}
public void setEng() {
	this.eng=eng;
}
public String getName() {
	return name;
}
public String getColour() {
	return colour;
}
public float getPrice() {
	return price;
}
public String getType() {
	return type;
}
public String getEng() {
	return eng;
}
}

